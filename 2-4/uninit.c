// Test to see what happens with uninitialized ints and floats
#include <stdio.h>

int main(void) {
  int a, b, c, d;
  float e, f, g, h;

  printf("%d\n", a);
  printf("%d\n", b);
  printf("%d\n", c);
  printf("%d\n", d);
  printf("%f\n", e);
  printf("%f\n", f);
  printf("%f\n", g);
  printf("%f\n", h);
}
